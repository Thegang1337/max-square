
public class MaxRootException extends Exception {
    public MaxRootException() {
        super();
    }
    public MaxRootException(final String message) {
        super(message);
    }
    public MaxRootException (final String message, final Throwable cause) {
        super(message, cause);
    }
    public MaxRootException (final Throwable cause) {
        super(cause);
    }
}
